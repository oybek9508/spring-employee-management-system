package com.spring_boot.employee_management.enums;

public enum LeaveEnum {
    SICK_LEAVE,       // For illness or health-related issues
    ANNUAL_LEAVE,     // For vacation or personal time off
    EMERGENCY_LEAVE,  // For urgent situations (e.g., family emergency)
    MATERNITY_LEAVE,  // For maternity leave after childbirth
    PATERNITY_LEAVE,  // For paternity leave
    COMPASSIONATE_LEAVE, // For family-related emergencies or death
    UNPAID_LEAVE
}
